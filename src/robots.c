{
    robotStart[16][2].gfx = (u16 *)robotAlt[version][0];
    robotStart[16][3].gfx = (u16 *)robotAlt[version][0];
    robotStart[16][4].gfx = (u16 *)robotAlt[version][0];
    robotStart[16][5].gfx = (u16 *)robotAlt[version][0];
    robotStart[17][4].gfx = (u16 *)robotAlt[version][1];
    robotStart[17][5].gfx = (u16 *)robotAlt[version][1];
    robotStart[17][6].gfx = (u16 *)robotAlt[version][1];
    robotStart[17][7].gfx = (u16 *)robotAlt[version][1];
}

void Robots_Init()
{
    int     count;
    ROBOT   *robot = &robotThis[0], *start = &robotStart[gameLevel][0];

    for (count = 0; count < 8; count++, robot++, start++)
    {
        robot->DoMove = start->DoMove;
        robot->DoSpg = DoNothing;
        if (start->nframes == -1)
        {
            robot->DoDraw = DoNothing;
            continue;
        }

        robot->DoDraw = DoRobotDraw;
        robot->x = start->x * 8;
        if (gameLevel == SKYLAB)
        {
            robot->y = start->min;
        }
        else
        {
            robot->y = start->y * 8;
            if (gameLevel == SPG)
            {
                robot->DoSpg = DoRobotSpg;
                robot->tile = start->y * 32 + start->x;
            }
        }

        robot->min = start->min;
        robot->max = start->max;

        robot->speed = start->speed;
        robot->ink = start->ink;
        robot->nframes = start->nframes;
        robot->frame = start->frame;

        robot->gfx = start->gfx;
    }
}
