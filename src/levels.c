BYTE        *conveyRotate[2];

EVENT       DoWall;

// these are used for tiles that transition to another tile: collapse, wall, etc
BYTE        *gfxSpace = (BYTE [8])SPACE;
BYTE        *gfxSolid = (BYTE [8]){255, 255, 255, 255, 255, 255, 255, 255};

TILE        levelTile[512], *curTile;
int         curPos;

BYTE        levelBG;

void DoCollapse()
{
    curPos = Video_Tile(curPos, curTile->gfx, curTile->paper, curTile->ink, curTile->data);
    Video_Tile(curPos, gfxSpace, levelBG, 0x0, 8 - curTile->data);
}

void DoTile()
{
    Video_Tile(curPos, curTile->gfx, curTile->paper, curTile->ink, 8);
}

void DoItem()
{
    curTile->ink = (curTile->ink & 3) + 3; // cycle the item colour
    DoTile();
}

void DoSpace()
{
    curTile->data = 0; // needed for SPG beam
    DoTile();
}

void DoWallTop()
{
    curPos = Video_Tile(curPos, curTile->gfx, curTile->paper, curTile->ink, curTile->data);
    Video_Tile(curPos, gfxSolid, 0x0, levelBG, 8 - curTile->data);
}

void DoWallBottom()
{
    curPos = Video_Tile(curPos, gfxSolid, 0x0, levelBG, curTile->data);
    Video_Tile(curPos, curTile->gfx + curTile->data, curTile->paper, curTile->ink, 8 - curTile->data);
}

int Level_GetSpgTile(int tile)
{
    return levelTile[tile].data;
}

void Level_SetSpgTiles(int tile, int count, int this)
{
    int adj = 1;

    for ( ; count > 0; count--, tile += adj, adj ^= 30)
    {
        levelTile[tile].data |= this;
    }
}

void Level_TileDelete(int tile)
{
    levelTile[tile].type = T_SPACE;
    levelTile[tile].gfx = gfxSpace;
    levelTile[tile].paper = levelBG;
    levelTile[tile].ink = 0x0;
    levelTile[tile].DoDraw = DoSpace;
}

void DoWallTick()
{
    levelTile[TILE2].data++;
    if (levelTile[TILE1].data-- > 0)
    {
        return;
    }

    Level_TileDelete(TILE1);
    Level_TileDelete(TILE2);
    Robots_Barrel();

    DoWall = DoNothing;
}

void Level_Switch(int tile)
{
    levelTile[tile].type = T_SWITCHON;
    levelTile[tile].ink = 0x4;
    levelTile[tile].gfx += 8;

    if (tile == 6)
    {
        levelTile[TILE1].data = 7;
        levelTile[TILE2].data = 1;
        levelTile[TILE1].DoDraw = DoWallTop;
        levelTile[TILE2].DoDraw = DoWallBottom;
        DoWall = DoWallTick;
    }
    else if (tile == 18) // right switch
    {
        Level_TileDelete(79);
        Level_TileDelete(80);
        Robots_Kong();
    }
}

void Level_CollapseTile(int tile)
{
    levelTile[tile].data--;
    if (levelTile[tile].data > 0)
    {
        return;
    }

    Level_TileDelete(tile);
}

int Level_GetTileType(int tile)
{
    int type = levelTile[tile].type;

    if (type == T_VOID)
    {
        return T_SOLID;
    }

    return type;
}

void Level_Ticker()
{
    *conveyRotate[0] = (*conveyRotate[0] << 2) | (*conveyRotate[0] >> 6); // left
    *conveyRotate[1] = (*conveyRotate[1] >> 2) | (*conveyRotate[1] << 6); // right

    DoWall();
}

void Level_Drawer()
{
    int     cell = 0;

    curTile = &levelTile[0];
    for (cell = 0; cell < 512; cell++, curTile++)
    {
        curPos = TILE2PIXEL(cell) + 7;
        curTile->DoDraw();
    }
}

void Level_Init()
{
    int     cell, item = 0;
    int     *data;
    INFO    *info;
    TILE    *tile = &levelTile[0];
    int     dir;

    data = &levelData[gameLevel][0];

    for (cell = 0; cell < 512; cell++, tile++, data++)
    {
        info = &levelInfo[gameLevel][*data];
        tile->type = info->type;
        tile->gfx = levelGfx[gameLevel][*data];
        if (info->type == T_VOID)
        {
            tile->DoDraw = DoNothing;
        }
        else
        {
            tile->data = 0;
            tile->ink = info->colour & 0x0f;
            tile->paper = info->colour >> 4;
            tile->DoDraw = DoTile;
        }

        if (info->type == T_ITEM)
        {
            tile->ink = (item & 3) + 3;
            item++;
            tile->DoDraw = DoItem;
        }
        else if (info->type == T_COLLAPSE)
        {
            tile->data = 8;
            tile->DoDraw = DoCollapse;
        }
        else if (info->type == T_SPACE)
        {
            tile->DoDraw = DoSpace;
        }
    }

    if (gameLevel == TWENTY)
    {
        Title_ScreenCopy();
    }

    DoWall = DoNothing;

    levelBG = levelInfo[gameLevel][0].colour >> 4;

    dir = levelInfo[gameLevel][1].type == T_CONVEYL ? 0 : 2;
    conveyRotate[0] = levelGfx[gameLevel][1] + dir;
    conveyRotate[1] = levelGfx[gameLevel][1] + 2 - dir;

    Video_PixelFill(129 * WIDTH, 8 * WIDTH, 0x7);
    Video_Write(0, "\x1\x7\x2\x0"); // set the colours
    Video_Write(129 * WIDTH + (WIDTH - Video_TextWidth(levelName[gameLevel])) / 2, levelName[gameLevel]);
}
